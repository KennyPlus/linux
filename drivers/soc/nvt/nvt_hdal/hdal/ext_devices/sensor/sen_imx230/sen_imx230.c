static void sen_get_modesel_imx230(CTL_SENDRV_GET_MODESEL_PARAM *data)
{
	if (data->if_type != CTL_SEN_IF_TYPE_MIPI) {
		DBG_ERR("if_type %d N.S. \r\n", data->if_type);
		return;
	}

	if (data->data_fmt != CTL_SEN_DATA_FMT_RGB) {
		DBG_ERR("data_fmt %d N.S. \r\n", data->data_fmt);
		return;
	}

	if (data->frame_num == 1) {
		if ((data->size.w <= 1920) && (data->size.h <= 1080)) {
			if (data->frame_rate <= 3000) {			
				data->mode = CTL_SEN_MODE_1;				
				return;
		  }
		} else if ((data->size.w <= 2668) && (data->size.h <= 2004)) {
			if (data->frame_rate <= 3000) {
				data->mode = CTL_SEN_MODE_4;
				return;
			}		  
		} else if ((data->size.w <= 2880) && (data->size.h <= 2160)) {
			if (data->frame_rate <= 3000) {
				data->mode = CTL_SEN_MODE_5;
				return;
			}		  
		} else if ((data->size.w <= 3840) && (data->size.h <= 2160)) {
			if (data->frame_rate <= 3000) {
				data->mode = CTL_SEN_MODE_3;			
				return;				
			}
		} else if ((data->size.w <= 5344) && (data->size.h <= 4014)) {
			if (data->frame_rate <= 2400) {			
				data->mode = CTL_SEN_MODE_2;			
				return;
			}
		} 
	}

	DBG_ERR("fail (frame_rate%d,size%d*%d,if_type%d,data_fmt%d,frame_num%d) \r\n"
			, data->frame_rate, data->size.w, data->size.h, data->if_type, data->data_fmt, data->frame_num);
	data->mode = CTL_SEN_MODE_1;
}

static UINT32 sen_calc_rowtime_step_imx230(CTL_SEN_ID id, CTL_SEN_MODE mode)
{
	UINT32 div_step = 0;

	if (mode >= SEN_MAX_MODE) {
		mode = cur_sen_mode[id];
	}

	div_step = 1;

	return div_step;
}

static UINT32 sen_calc_rowtime_imx230(CTL_SEN_ID id, CTL_SEN_MODE mode)
{
	UINT32 row_time = 0;

	if (mode >= SEN_MAX_MODE) {
		mode = cur_sen_mode[id];
	}

	//Precision * 10
	row_time = 1000 * (mode_basic_param[mode].signal_info.hd_period) / ((speed_param[mode].pclk) * 4 / 10000);

	return row_time;
}

static void sen_get_rowtime_imx230(CTL_SEN_ID id, CTL_SENDRV_GET_MODE_ROWTIME_PARAM *data)
{
	data->row_time_step = sen_calc_rowtime_step_imx230(id, data->mode);	
	data->row_time = sen_calc_rowtime_imx230(id, data->mode) * (data->row_time_step);
}


static UINT32 sen_calc_chgmode_vd_imx230(CTL_SEN_ID id, UINT32 fps)
{
	UINT32 sensor_vd;

	if (1 > fps) {
		DBG_ERR("sensor fps can not small than (%d),change to dft sensor fps (%d) \r\n", fps, mode_basic_param[cur_sen_mode[id]].dft_fps);	
		fps = mode_basic_param[cur_sen_mode[id]].dft_fps;
	}
	sensor_vd = (mode_basic_param[cur_sen_mode[id]].signal_info.vd_period) * (mode_basic_param[cur_sen_mode[id]].dft_fps) / fps;

  //DBG_ERR("fps= %d \r\n", fps);

	sen_set_chgmode_fps_imx230(id, fps);
	sen_set_cur_fps_imx230(id, fps);

	if (sensor_vd > MAX_VD_PERIOD) {
		DBG_ERR("sensor vd out of sensor driver range (%d) \r\n", sensor_vd);
		sensor_vd = MAX_VD_PERIOD;
		fps = (mode_basic_param[cur_sen_mode[id]].signal_info.vd_period) * (mode_basic_param[cur_sen_mode[id]].dft_fps) / sensor_vd;
		sen_set_chgmode_fps_imx230(id, fps);
		sen_set_cur_fps_imx230(id, fps);
	}

	if(sensor_vd < (mode_basic_param[cur_sen_mode[id]].signal_info.vd_period)) {
		DBG_ERR("sensor vd out of sensor driver range (%d) \r\n", sensor_vd);
		sensor_vd = mode_basic_param[cur_sen_mode[id]].signal_info.vd_period;
		sen_set_chgmode_fps_imx230(id, mode_basic_param[cur_sen_mode[id]].dft_fps);
		sen_set_cur_fps_imx230(id, mode_basic_param[cur_sen_mode[id]].dft_fps);
	}

	return sensor_vd;
}

static UINT32 sen_calc_exp_vd_imx230(CTL_SEN_ID id, UINT32 fps)
{
	UINT32 sensor_vd;

	if (1 > fps) {
		DBG_ERR("sensor fps can not small than (%d),change to dft sensor fps (%d) \r\n", fps, mode_basic_param[cur_sen_mode[id]].dft_fps);	
		fps = mode_basic_param[cur_sen_mode[id]].dft_fps;
	}
	sensor_vd = (mode_basic_param[cur_sen_mode[id]].signal_info.vd_period) * (mode_basic_param[cur_sen_mode[id]].dft_fps) / fps;

	if (sensor_vd > MAX_VD_PERIOD) {
		DBG_ERR("sensor vd out of sensor driver range (%d) \r\n", sensor_vd);
		sensor_vd = MAX_VD_PERIOD;
	
	}

	if(sensor_vd < (mode_basic_param[cur_sen_mode[id]].signal_info.vd_period)) {
		DBG_ERR("sensor vd out of sensor driver range (%d) \r\n", sensor_vd);
		sensor_vd = mode_basic_param[cur_sen_mode[id]].signal_info.vd_period;
	}

	return sensor_vd;
}

static void sen_set_gain_imx230(CTL_SEN_ID id, void *param)
{
	ISP_SENSOR_CTRL *sensor_ctrl = (ISP_SENSOR_CTRL *)param;
	UINT32 data1[ISP_SEN_MFRAME_MAX_NUM] = {0};
	UINT32 data2[ISP_SEN_MFRAME_MAX_NUM] = {0};
	UINT32 data3[ISP_SEN_MFRAME_MAX_NUM] = {0};	
	UINT32 frame_cnt, total_frame;
	UINT32 analog_gain = 0, digital_gain_h = 0, digital_gain_l = 0;
	CTL_SEN_CMD cmd;
	ER rt = E_OK;

	sensor_ctrl_last[id].gain_ratio[0] = sensor_ctrl->gain_ratio[0];
	sensor_ctrl_last[id].gain_ratio[1] = sensor_ctrl->gain_ratio[1];
	// Calculate sensor gain
	if (mode_basic_param[cur_sen_mode[id]].frame_num == 0) {
		DBG_WRN("total_frame = 0, force to 1 \r\n");
		total_frame = 1;
	} else {
		total_frame = mode_basic_param[cur_sen_mode[id]].frame_num;
	}

	for (frame_cnt = 0; frame_cnt < total_frame; frame_cnt++) {
		if (sensor_ctrl->gain_ratio[frame_cnt] < (mode_basic_param[cur_sen_mode[id]].gain.min)) {
			sensor_ctrl->gain_ratio[frame_cnt] = (mode_basic_param[cur_sen_mode[id]].gain.min);
		} else if (sensor_ctrl->gain_ratio[frame_cnt] > (mode_basic_param[cur_sen_mode[id]].gain.max)) {
			sensor_ctrl->gain_ratio[frame_cnt] = (mode_basic_param[cur_sen_mode[id]].gain.max);			
		}		
		if ((sensor_ctrl->gain_ratio[frame_cnt]) > 8000) {
		  analog_gain = 448;
		  digital_gain_h = (sensor_ctrl->gain_ratio[frame_cnt]) / 8000;
		  digital_gain_l = ((100 * (sensor_ctrl->gain_ratio[frame_cnt]) / 8) - (digital_gain_h * 100000)) * 256 / 100000;
		} else {
		  analog_gain = 512 - (512000/(sensor_ctrl->gain_ratio[frame_cnt]));
		  digital_gain_h = 1;
		  digital_gain_l = 0;
		}
    
    if(0x0F < digital_gain_h){
        digital_gain_h = 0xF;
    } else if (0x01 > digital_gain_h){
        digital_gain_h = 0x01; 
    } 
    
    if(0xFF < digital_gain_l){
        digital_gain_l = 0xFF;
    }		
		
		data1[frame_cnt] = analog_gain;
		data2[frame_cnt] = digital_gain_h;
		data3[frame_cnt] = digital_gain_l; 		
	}

	// Write sensor gain
	cmd = sen_set_cmd_info_imx230(0x0204, 1, (data1[0] >> 8) & 0x01, 0x0);
	rt |= sen_write_reg_imx230(id, &cmd);
	cmd = sen_set_cmd_info_imx230(0x0205, 1, data1[0] & 0xFF, 0x0);
	rt |= sen_write_reg_imx230(id, &cmd);	
	
	cmd = sen_set_cmd_info_imx230(0x020E, 1, data2[0] & 0x0F, 0x0);
	rt |= sen_write_reg_imx230(id, &cmd);
	cmd = sen_set_cmd_info_imx230(0x020F, 1, data3[0] & 0xFF, 0x0);
	rt |= sen_write_reg_imx230(id, &cmd);		
	cmd = sen_set_cmd_info_imx230(0x0210, 1, data2[0] & 0x0F, 0x0);
	rt |= sen_write_reg_imx230(id, &cmd);
	cmd = sen_set_cmd_info_imx230(0x0211, 1, data3[0] & 0xFF, 0x0);
	rt |= sen_write_reg_imx230(id, &cmd);		
	cmd = sen_set_cmd_info_imx230(0x0212, 1, data2[0] & 0x0F, 0x0);
	rt |= sen_write_reg_imx230(id, &cmd);
	cmd = sen_set_cmd_info_imx230(0x0213, 1, data3[0] & 0xFF, 0x0);
	rt |= sen_write_reg_imx230(id, &cmd);		
	cmd = sen_set_cmd_info_imx230(0x0214, 1, data2[0] & 0x0F, 0x0);
	rt |= sen_write_reg_imx230(id, &cmd);
	cmd = sen_set_cmd_info_imx230(0x0215, 1, data3[0] & 0xFF, 0x0);
	rt |= sen_write_reg_imx230(id, &cmd);

	if (rt != E_OK) {
		DBG_ERR("write register error %d \r\n", (INT)rt);
	}
}

static void sen_set_expt_imx230(CTL_SEN_ID id, void *param)
{
	ISP_SENSOR_CTRL *sensor_ctrl = (ISP_SENSOR_CTRL *)param;
	UINT32 line[ISP_SEN_MFRAME_MAX_NUM];
	UINT32 frame_cnt, total_frame;
	CTL_SEN_CMD cmd;
	UINT32 expt_time = 0, sensor_vd = 0, chgmode_fps, cur_fps = 0, clac_fps = 0;
	UINT32 t_row = 0;
	ER rt = E_OK;

	sensor_ctrl_last[id].exp_time[0] = sensor_ctrl->exp_time[0];
	sensor_ctrl_last[id].exp_time[1] = sensor_ctrl->exp_time[1];

	if (mode_basic_param[cur_sen_mode[id]].frame_num == 0) {
		DBG_WRN("total_frame = 0, force to 1 \r\n");
		total_frame = 1;
	} else {
		total_frame = mode_basic_param[cur_sen_mode[id]].frame_num;
	}

	// Calculate exposure line
	for (frame_cnt = 0; frame_cnt < total_frame; frame_cnt++) {
		// Calculates the exposure setting
		t_row = sen_calc_rowtime_imx230(id, cur_sen_mode[id]);
		if (0 == t_row) {
			DBG_WRN("t_row  = 0, must >= 1 \r\n");
			t_row = 1;
		}
		line[frame_cnt] = (sensor_ctrl->exp_time[frame_cnt]) * 10 / t_row;

		// Limit minimun exposure line
		if (line[frame_cnt] < MIN_EXPOSURE_LINE) {
			line[frame_cnt] = MIN_EXPOSURE_LINE;
		}
	}

	// Write exposure line
	// Get fps
	chgmode_fps = sen_get_chgmode_fps_imx230(id);

	// Calculate exposure time
	t_row = sen_calc_rowtime_imx230(id, cur_sen_mode[id]);
	expt_time = (line[0]) * t_row / 10;


	// Calculate fps
	if (0 == expt_time) {
		DBG_WRN("expt_time  = 0, must >= 1 \r\n");		
		expt_time = 1;
	}
	clac_fps = 100000000 / expt_time;

	cur_fps = (clac_fps < chgmode_fps) ? clac_fps : chgmode_fps;
	sen_set_cur_fps_imx230(id, cur_fps);

	// Calculate new vd
	sensor_vd = sen_calc_exp_vd_imx230(id, cur_fps);

	//Check max vts
	if (sensor_vd > MAX_VD_PERIOD) {
		DBG_ERR("max vts overflow\r\n");
		sensor_vd = MAX_VD_PERIOD;
	}

	cmd = sen_set_cmd_info_imx230(0x0340, 1, (sensor_vd >> 8) & 0xFF, 0);
	rt |= sen_write_reg_imx230(id, &cmd);
	cmd = sen_set_cmd_info_imx230(0x0341, 1, sensor_vd & 0xFF, 0);
	rt |= sen_write_reg_imx230(id, &cmd);	


	if (line[0] > MAX_EXPOSURE_LINE) {
		DBG_ERR("max line overflow \r\n");
		line[0] = MAX_EXPOSURE_LINE;
	}

	// Calculate shs1 & vmax
	if (line[0] > (sensor_vd - NON_EXPOSURE_LINE)) {
		line[0] = sensor_vd - NON_EXPOSURE_LINE;
	}

	cmd = sen_set_cmd_info_imx230(0x0202, 1, (line[0] >> 8) & 0xFF, 0);
	rt |= sen_write_reg_imx230(id, &cmd);
	cmd = sen_set_cmd_info_imx230(0x0203, 1, line[0] & 0xFF, 0);
	rt |= sen_write_reg_imx230(id, &cmd);
	
	if (rt != E_OK) {
		DBG_ERR("write register error %d \r\n", (INT)rt);
	}
}

